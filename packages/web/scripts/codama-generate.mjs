// Generate the apis_program TypeScript client from the on-chain IDL.
//
// Reads:  ../program/target/idl/apis_program.json
// Writes: app/lib/generated/apis-program/
//
// Re-run after every `anchor build` if the program's IDL changed:
//   pnpm --filter web codama:generate
//
// The generated output is committed to git so editor IntelliSense + CI work
// without a build step. If you'd rather treat it as a build artifact, add
// app/lib/generated/ to .gitignore and put `pnpm codama:generate` in `prebuild`.

import { createFromRoot } from "codama";
import { rootNodeFromAnchor } from "@codama/nodes-from-anchor";
import { renderVisitor } from "@codama/renderers-js";
import { readFileSync, rmSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const idlPath = resolve(__dirname, "../../program/target/idl/apis_program.json");
const outputDir = resolve(__dirname, "../app/lib/generated/apis-program");

const idl = JSON.parse(readFileSync(idlPath, "utf-8"));
const codama = createFromRoot(rootNodeFromAnchor(idl));
await codama.accept(renderVisitor(outputDir));

// Strip the autogenerated package.json — its `main` points to src/index.ts
// which doesn't exist (entry is src/generated/index.ts). We import from
// the file directly via the @/lib/apis-program barrel; no sub-package needed.
rmSync(resolve(outputDir, "package.json"), { force: true });

console.log(`✓ Generated apis_program client at ${outputDir}`);
